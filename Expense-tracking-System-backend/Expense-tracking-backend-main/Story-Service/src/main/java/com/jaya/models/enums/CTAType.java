package com.jaya.models.enums;

/**
 * Call-to-action button types for stories
 */
public enum CTAType {
    VIEW_REPORT, // Navigate to reports
    GO_TO_BUDGET, // Navigate to budget page
    VIEW_EXPENSE, // View specific expense
    VIEW_BILL, // View specific bill
    VIEW_CATEGORY, // View category breakdown
    MANAGE_BUDGETS, // Open budget management
    ADD_EXPENSE, // Quick add expense
    PAY_BILL, // Pay a bill
    LEARN_MORE, // External link or help
    DISMISS, // Dismiss the story
    CUSTOM // Custom action
}
